        $('.datum-confirm').bind('click', function(event) {
            var $that = $(this);
            layer.confirm('资料确认无误吗？', {
                title:false,
                btn: ['确认','取消']
            }, function(){
                window.location.href="new_uct_works53.html";
            })
        });
        // 点击修改意见
        function stopPropagation(e) {
            if (e.stopPropagation)
                e.stopPropagation();
            else
                e.cancelBubble = true;
        }
        $('.datum-change').bind('click',function(e) {
            var $that = $(this);
            stopPropagation(e);
            $('.cover').fadeIn();
        });
        $(document).click(function(e) {
            stopPropagation(e);
            $('.cover').fadeOut();
        });
        $('.cover-pop').click(function(e) {
            stopPropagation(e);
        });
        $('.cover-cancel').click(function(event) {
            $(this).closest('.cover').fadeOut();
        });

    })
</script>
